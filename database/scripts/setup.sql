-- USER_ROLES
CREATE TABLE USER_ROLES (
    USER_ID INTEGER REFERENCES USERS(ID) NOT NULL,
    ROLE_ID INTEGER REFERENCES ROLE_REF(ID) NOT NULL
);

-- DECISION_USERS
CREATE TABLE DECISION_USERS (
    DECISION_ID INTEGER REFERENCES DECISION(ID) NOT NULL,
    USER_ID INTEGER REFERENCES USERS(ID) NOT NULL
);
