CREATE TABLE USERS (
USERNAME VARCHAR(32) UNIQUE PRIMARY KEY NOT NULL,
LASTNAME VARCHAR(32) NOT NULL,
FIRSTNAME VARCHAR(32) NOT NULL,
EMAIL VARCHAR(32) NOT NULL );

