    ID INTEGER UNIQUE PRIMARY KEY NOT NULL,
    DECISION_ID INTEGER REFERENCES DECISION(ID) NOT NULL,
    USERNAME VARCHAR(32) REFERENCES USERS(USERNAME) NOT NULL
);

